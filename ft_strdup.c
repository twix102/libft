#include "libft.h"

static char	*ft_strcpy(char *dest, const char *src)
{
	size_t	i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

char		*ft_strdup(const char *s1)
{
	char	*dest;

	dest = malloc((ft_strlen(s1) + 1) * sizeof(char));
	return (dest ? ft_strcpy(dest, s1) : dest);
}
